package J07_StringManipulation;

public class C16_UppercaseLowercase {
    public static void main(String[] args) {


        /*
        touppercase : verilen String in tüm harflerini büyük harfe çevirir
        tolowercase : verilen String in tüm harflerini küçük harfe çevirir.
        toignoreCase : verilen String in büyük küçük harf duyarlılığını kaldırır.
         */

            String str = "Java'canlara selam olsun bolcana offer";
        System.out.println(str);
        System.out.println(str.toUpperCase());
        System.out.println(str.toLowerCase());




    }
}
